export const BRACHYCEPHALIC_CAT_BREEDS = new Set([
  'Persian',
  'Exotic Shorthair',
  'Himalayan',
  'Scottish Fold',
  'British Shorthair',
  'Burmese',
]);

export function isBrachycephalic(breed: string, species: 'dog' | 'cat'): boolean {
  if (species === 'dog') return BRACHYCEPHALIC_DOG_BREEDS.has(breed);
  return BRACHYCEPHALIC_CAT_BREEDS.has(breed);
}
